# In a division problem the quotient is often rounded to a whole number when divided in python. If you make the numbers you are dividing floats than will also be a float. 

cucumbers = 100
num_people = 6

whole_cucumbers_per_person = cucumbers / num_people

float_cucumbers_per_person = float(cucumbers) / num_people

print ("float_cucumbers_per_person")